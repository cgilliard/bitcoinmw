const SECP256K1_FLAGS_TYPE_COMPRESSION: u32 = 1 << 1;
const SECP256K1_FLAGS_BIT_COMPRESSION: u32 = 1 << 8;
pub const SECP256K1_EC_COMPRESSED: u32 =
	SECP256K1_FLAGS_TYPE_COMPRESSION | SECP256K1_FLAGS_BIT_COMPRESSION;

